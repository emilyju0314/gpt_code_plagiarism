#include <bits/stdc++.h>
using namespace std;
signed main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  cout.tie(0);
  ;
  long long t;
  cin >> t;
  while (t--) {
    long long n;
    cin >> n;
    cout << n << "\n";
    for (long long i = 1; i <= n; i++) cout << i << " ";
    cout << "\n";
  }
}
