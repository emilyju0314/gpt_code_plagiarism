bool is_atom_char(char c)
{
    return (c >= 'A' && c <= 'Z')
        || (c >= 'a' && c <= 'z')
        || (c >= '0' && c <= '9')
        || c == '!' || c == '#'
        || c == '$' || c == '%'
        || c == '&' || c == '\''
        || c == '*' || c == '+'
        || c == '-' || c == '/'
        || c == '=' || c == '?'
        || c == '^' || c == '_'
        || c == '`' || c == '{'
        || c == '|' || c == '}'
        || c == '~';
}